// CPP program to find modulo of floating
// point numbers using library function.
#include <bits/stdc++.h>
using namespace std;

// Driver Function
int main() {
	
	// Def variables
	double a = 9.7, b = 2.3;
	
	// Use fmod function
	cout << "Modulus of a and b: " << fmod(a, b);
	
	return 0;
} //End driver
